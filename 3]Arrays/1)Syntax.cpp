#include <iostream>
using namespace std;
int main() {
    // Declare and initialize an array of integers
    int numbers[5] = {10, 20, 30, 40, 50};

    // Access and print each element of the array
    for (int i = 0; i < 5; i++) {
        cout << "Element at index " << i << ": " << numbers[i] << endl;
    }

    return 0;
}
// This code demonstrates the syntax for declaring, initializing, and accessing elements of an array in C++.